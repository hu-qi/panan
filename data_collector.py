@dataclass
class CoffeeShopAnalysis:
    """咖啡馆可行性分析"""

    # 基础参数
    total_population: float = 17.9  # 万人
    urbanization_rate: float = 55.9  # %
    aging_rate_60plus: float = 23.44  # %
    annual_income_per_capita: float = 42915  # 元
    annual_consumption_per_capita: float = 34139  # 元
    catering_growth_rate: float = 20.1  # %
    tourist_weekend_daily: float = 2500  # 高铁周末日均旅客

    def estimate_core_customer_base(self) -> Dict:
        """
        估算核心咖啡客群
        假设: 18-40岁人口约占总人口30%
              咖啡渗透率对标三线城市约10%
        """
        total = self.total_population * 10000

        # 根据年龄结构估算
        age_15_59 = total * 0.6358  # 132,658人
        # 18-40岁 粗略估算为15-59岁的45%
        age_18_40 = age_15_59 * 0.45
        # 常住+本地年轻人
        urban_young = total * (self.urbanization_rate / 100) * 0.30

        # 咖啡消费渗透率假设（小县城偏低）
        penetration_low = 0.05
        penetration_mid = 0.10
        penetration_high = 0.15

        return {
            "常住总人口": f"{total:.0f}",
            "15-59岁人口": f"{age_15_59:.0f}",
            "估算18-40岁核心群": f"{age_18_40:.0f}",
            "城区年轻人(城镇化校正)": f"{urban_young:.0f}",
            "潜在咖啡用户_乐观15%": f"{age_18_40 * penetration_high:.0f}",
            "潜在咖啡用户_中性10%": f"{age_18_40 * penetration_mid:.0f}",
            "潜在咖啡用户_保守5%": f"{age_18_40 * penetration_low:.0f}",
        }

    def estimate_revenue(self) -> Dict:
        """估算单店收入模型"""
        # 假设县城小店，日均60-120杯
        scenarios = {
            "保守": {"日均杯数": 60, "客单价": 18},
            "中性": {"日均杯数": 90, "客单价": 22},
            "乐观": {"日均杯数": 120, "客单价": 25},
        }

        result = {}
        for name, params in scenarios.items():
            daily_revenue = params["日均杯数"] * params["客单价"]
            monthly_revenue = daily_revenue * 30
            annual_revenue = monthly_revenue * 12

            # 咖啡店毛利率通常50-65%
            gross_margin = 0.55
            monthly_gross = monthly_revenue * gross_margin

            # 租金假设 3000-5000元/月（磐安县城水平）
            rent = 3500
            labor = 2 * 4000  # 2个员工
            utilities = 2000
            monthly_cost = rent + labor + utilities

            monthly_net = monthly_gross - monthly_cost

            result[name] = {
                "日均杯数": params["日均杯数"],
                "客单价(元)": params["客单价"],
                "日营收(元)": f"{daily_revenue:.0f}",
                "月营收(元)": f"{monthly_revenue:.0f}",
                "年营收(元)": f"{annual_revenue:.0f}",
                "月毛利(元)": f"{monthly_gross:.0f}",
                "月固定成本(元)": f"{monthly_cost:.0f}",
                "月净利润(元)": f"{monthly_net:.0f}",
                "年净利润(元)": f"{monthly_net * 12:.0f}",
                "回本周期(月)": f"{200000 / monthly_net:.1f}" if monthly_net > 0 else "亏损",
            }
        return result

    def swot_matrix(self) -> str:
        """SWOT分析文本"""
        return """
==========================================================
                  S W O T  矩 阵
==========================================================
优势(S)                      劣势(W)
  + 县城城区咖啡馆空白           - 常住人口仅17.9万，负增长
  + 杭温高铁开通带来客流        - 老龄化23.44%，核心客群小
  + 消费增速全市第一(社消+32.8%)- 人均收入仅浙江平均的64%
  + 生态环境契合咖啡慢生活      - 高等学历比例低(10.44%)
  + 已有"村咖"验证需求          - 咖啡消费习惯待培育
----------------------------------------------------------
机会(O)                      威胁(T)
  + 旅游经济高增长(+10%)        - "村咖"已成气候，需差异化
  + 县城品质消费升级窗口期      - 人口持续外流
  + 高铁持续释放出行红利        - 瑞幸/库迪可能下沉
  + 可融入药膳/茶文化差异化     - 消费升级速度不确定性
==========================================================
"""

    def run(self) -> str:
        """运行完整分析"""
        lines = []
        lines.append("=" * 60)
        lines.append("  磐安县咖啡馆可行性量化分析")
        lines.append(f"  分析时间: {datetime.now().strftime('%Y-%m-%d %H:%M')}")
        lines.append("=" * 60)

        lines.append("\n【一、客群估算】")
        for k, v in self.estimate_core_customer_base().items():
            lines.append(f"  {k}: {v}")

        lines.append("\n【二、收入模型（假设20万投资）】")
        for scenario, metrics in self.estimate_revenue().items():
            lines.append(f"\n  --- {scenario}方案 ---")
            for k, v in metrics.items():
                lines.append(f"    {k}: {v}")

        lines.append(self.swot_matrix())
        return "\n".join(lines)


